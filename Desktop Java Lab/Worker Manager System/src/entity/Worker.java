package entity;

/**
 *
 * @author Admin
 */
public class Worker {
    private int id;
    private String name;
    private int gender;
    private String role;
    private String image;

    public Worker() {
    }

    public Worker(int id, String name, int gender, String role, String image) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.role = role;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
